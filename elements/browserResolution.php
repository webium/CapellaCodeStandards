    <h2>Browser Resolution</h2>
        <p>Along with catering to browsers, developers must stay conscious of the screen resolutions of their audience. As monitor screens get larger, the breadth of resolutions grows as well. Below are some guidelines to help you in working with resolutions.</p>
    
    <p><strong>1024px resolution</strong></p>
    
    <ul>
        <li><i class="icon-minus"></i>Fold estimated at 570px.</li>
        <li><i class="icon-minus"></i>Optimal width: 960px - Has comfortable padding on both sides, is divisible by many numbers, and also plays well with <a href="http://www.iab.net/iab_products_and_industry_services/1421/1443/1452" target="_blank">IAB ad standard widths</a></li>
        <li><i class="icon-minus"></i>Larger width: 970px - Still has some padding on both sides in most browsers. The math plays well with the <a href="http://en.wikipedia.org/wiki/Golden_ratio" target="_blank">Golden Ratio</a></li>
        <li><i class="icon-minus"></i>Maximum width: 996px - Without incurring any horizontal scrollbars in the major browsers. <a href="http://www.nealgrosskopf.com/tech/thread.php?pid=43" target="_blank">Based on the research here</a> the maximum is 1003 px wide if you don't want a horizontal scrollbar.</li>
    </ul>
    
    
